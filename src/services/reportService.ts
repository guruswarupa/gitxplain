/**
 * Generate JSON format report
 */
function generateJsonReport(data: ReportData): string {
  const { project, commits, stories, insights, generatedAt } = data;
  
  const authors = getUniqueAuthors(commits);
  const commitsByType = getCommitsByType(commits);
  
  const report = {
    meta: {
      generatedAt: generatedAt.toISOString(),
      generator: 'Commit Story Desktop',
      version: '1.0.0',
    },
    project: {
      name: project.name,
      path: project.path,
    },
    summary: {
      totalCommits: commits.length,
      totalContributors: authors.length,
      totalStories: stories.length,
      totalFilesChanged: getUniqueFiles(commits).size,
      dateRange: {
        earliest: commits.length > 0 ? commits[commits.length - 1].date : null,
        latest: commits.length > 0 ? commits[0].date : null,
      },
    },
    commitsByType: Object.fromEntries(commitsByType),
    contributors: authors.map(author => ({
      name: author,
      commits: commits.filter(c => c.author === author).length,
    })),
    stories: stories.map(s => ({
      id: s.id,
      title: s.title,
      type: s.type,
      summary: s.summary,
      timestamp: s.timestamp,
      commitCount: s.commits.length,
      fileCount: s.files.length,
      commits: s.commits.map(c => c.hash),
      files: s.files,
    })),
    commits: commits.map(c => ({
      hash: c.hash,
      author: c.author,
      date: c.date,
      message: c.message,
    })),
  };
  
  return JSON.stringify(report, null, 2);
}

// Helper functions

function getUniqueAuthors(commits: Commit[]): string[] {
  return [...new Set(commits.map(c => c.author))];
}

function getUniqueFiles(commits: Commit[]): Set<string> {
  const files = new Set<string>();
  commits.forEach(c => {
    c.files?.forEach(f => files.add(f.path));
  });
  return files;
}

function getCommitsByType(commits: Commit[]): Map<string, number> {
  const types = new Map<string, number>();
  
  commits.forEach(commit => {
    const type = extractCommitType(commit.message);
    types.set(type, (types.get(type) || 0) + 1);
  });
  
  return types;
}

function extractCommitType(message: string): string {
  const match = message.match(/^(feat|fix|docs|refactor|style|test|chore|build|ci|perf)(\(.+\))?:/i);
  return match ? match[1].toLowerCase() : 'other';
}

function getInsightsSummary(commits: Commit[], stories: Story[]): string {
  const authors = getUniqueAuthors(commits);
  const types = getCommitsByType(commits);
  
  const featureCount = types.get('feat') || 0;
  const fixCount = types.get('fix') || 0;
  
  let summary = '';
  
  if (featureCount > 0) {
    summary += `${featureCount} new feature${featureCount > 1 ? 's were' : ' was'} added. `;
  }
  if (fixCount > 0) {
    summary += `${fixCount} bug fix${fixCount > 1 ? 'es were' : ' was'} implemented. `;
  }
  if (authors.length > 1) {
    summary += `${authors.length} contributors collaborated on this project.`;
  }
  
  return summary || 'Various improvements and changes were made.';
}

function getCommitsByTypeTable(commits: Commit[]): string {
  const types = getCommitsByType(commits);
  const sorted = [...types.entries()].sort((a, b) => b[1] - a[1]);
  
  let table = '| Type | Count | Percentage |\n|------|-------|------------|\n';
  
  sorted.forEach(([type, count]) => {
    const pct = ((count / commits.length) * 100).toFixed(1);
    table += `| ${type.charAt(0).toUpperCase() + type.slice(1)} | ${count} | ${pct}% |\n`;
  });
  
  return table;
}

function getContributorsTable(commits: Commit[]): string {
  const authors = getUniqueAuthors(commits);
  const authorCounts = authors.map(author => ({
    author,
    count: commits.filter(c => c.author === author).length,
  })).sort((a, b) => b.count - a.count);
  
  let table = '| Contributor | Commits | Percentage |\n|-------------|---------|------------|\n';
  
  authorCounts.forEach(({ author, count }) => {
    const pct = ((count / commits.length) * 100).toFixed(1);
    table += `| ${author} | ${count} | ${pct}% |\n`;
  });
  
  return table;
}

function getContributorRows(commits: Commit[]): string {
  const authors = getUniqueAuthors(commits);
  const authorCounts = authors.map(author => ({
    author,
    count: commits.filter(c => c.author === author).length,
  })).sort((a, b) => b.count - a.count);
  
  return authorCounts.map(({ author, count }) => {
    const pct = ((count / commits.length) * 100).toFixed(1);
    return `<tr><td>${escapeHtml(author)}</td><td>${count}</td><td>${pct}%</td></tr>`;
  }).join('');
}

function getTypeRows(commits: Commit[]): string {
  const types = getCommitsByType(commits);
  const sorted = [...types.entries()].sort((a, b) => b[1] - a[1]);
  
  return sorted.map(([type, count]) => {
    const pct = ((count / commits.length) * 100).toFixed(1);
    return `<tr><td>${type.charAt(0).toUpperCase() + type.slice(1)}</td><td>${count}</td><td>${pct}%</td></tr>`;
  }).join('');
}

function getTimelineSection(commits: Commit[]): string {
  const byMonth = new Map<string, number>();
  
  commits.forEach(commit => {
    const date = new Date(commit.date);
    const key = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}`;
    byMonth.set(key, (byMonth.get(key) || 0) + 1);
  });
  
  const sorted = [...byMonth.entries()].sort((a, b) => a[0].localeCompare(b[0]));
  
  let timeline = '| Month | Commits |\n|-------|--------|\n';
  sorted.forEach(([month, count]) => {
    const [year, m] = month.split('-');
    const monthName = new Date(parseInt(year), parseInt(m) - 1).toLocaleDateString('en-US', { month: 'short', year: 'numeric' });
    timeline += `| ${monthName} | ${count} |\n`;
  });
  
  return timeline;
}

function escapeMarkdown(text: string): string {
  return text.replace(/[|\\`*_{}[\]()#+\-.!]/g, '\\$&');
}

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

