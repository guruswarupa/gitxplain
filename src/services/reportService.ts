/**
 * Report Generation Service
 * Creates comprehensive project reports from commit history and stories
 */

import { Project, Commit, Story, RepoInsights } from '../models';

export interface ReportOptions {
  includeCommitList: boolean;
  includeStories: boolean;
  includeInsights: boolean;
  includeFileChanges: boolean;
  dateRange?: { start: Date; end: Date };
  format: 'markdown' | 'html' | 'json';
}

export interface ReportData {
  project: Project;
  commits: Commit[];
  stories: Story[];
  insights: RepoInsights | null;
  generatedAt: Date;
  options: ReportOptions;
}

const defaultOptions: ReportOptions = {
  includeCommitList: true,
  includeStories: true,
  includeInsights: true,
  includeFileChanges: true,
  format: 'markdown',
};

/**
 * Generate a comprehensive project report
 */
export function generateReport(data: ReportData): string {
  const { format } = data.options;
  
  switch (format) {
    case 'html':
      return generateHtmlReport(data);
    case 'json':
      return generateJsonReport(data);
    case 'markdown':
    default:
      return generateMarkdownReport(data);
  }
}

/**
 * Generate Markdown format report
 */
function generateMarkdownReport(data: ReportData): string {
  const { project, commits, stories, insights, generatedAt, options } = data;
  
  let report = `# Project Report: ${project.name}

> Generated on ${generatedAt.toLocaleString()}

## Overview

- **Repository:** ${project.path}
- **Total Commits Analyzed:** ${commits.length}
- **Stories Generated:** ${stories.length}

---

`;

  // Executive Summary
  if (options.includeInsights && insights) {
    report += `## Executive Summary

This report covers ${commits.length} commits from the project "${project.name}". 
${getInsightsSummary(commits, stories)}

### Key Metrics

| Metric | Value |
|--------|-------|
| Total Commits | ${commits.length} |
| Contributors | ${getUniqueAuthors(commits).length} |
| Stories | ${stories.length} |
| Files Changed | ${getUniqueFiles(commits).size} |

---

`;
  }

  // Stories Section
  if (options.includeStories && stories.length > 0) {
    report += `## Commit Stories

${stories.map((story, index) => `
### ${index + 1}. ${story.title}

**Type:** ${story.type.toUpperCase()} | **Date:** ${new Date(story.timestamp).toLocaleDateString()} | **Commits:** ${story.commits.length}

${story.summary}

<details>
<summary>View ${story.commits.length} commits</summary>

${story.commits.map(c => `- \`${c.hash.substring(0, 7)}\` ${c.message}`).join('\n')}

</details>

**Files affected:** ${story.files.length}
`).join('\n')}

---

`;
  }

  // Commits by Type
  report += `## Commits by Type

${getCommitsByTypeTable(commits)}

---

`;

  // Contributors
  report += `## Contributors

${getContributorsTable(commits)}

---

`;

  // Timeline
  report += `## Activity Timeline

${getTimelineSection(commits)}

---

`;

  // Commit List
  if (options.includeCommitList) {
    report += `## Full Commit List

| Hash | Author | Date | Message |
|------|--------|------|---------|
${commits.slice(0, 100).map(c => 
  `| \`${c.hash.substring(0, 7)}\` | ${c.author} | ${new Date(c.date).toLocaleDateString()} | ${escapeMarkdown(c.message.substring(0, 60))}${c.message.length > 60 ? '...' : ''} |`
).join('\n')}

${commits.length > 100 ? `\n*Showing 100 of ${commits.length} commits*\n` : ''}

---

`;
  }

  // Footer
  report += `
---

*Report generated by Commit Story Desktop*
`;

  return report;
}

